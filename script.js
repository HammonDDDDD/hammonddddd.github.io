const languageButtons = document.querySelectorAll(".lang-button");
const translatableNodes = document.querySelectorAll("[data-i18n]");
const projectGrid = document.querySelector("#projectGrid");

function getInitialLanguage() {
  const saved = localStorage.getItem("portfolio-language");
  if (saved === "ru" || saved === "en") return saved;
  return navigator.language.toLowerCase().startsWith("ru") ? "ru" : "en";
}

function translatePage(lang) {
  document.documentElement.lang = lang;
  document.title = translations[lang]["document.title"];

  translatableNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[lang][key]) {
      node.textContent = translations[lang][key];
    }
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });

  renderProjects(lang);
  localStorage.setItem("portfolio-language", lang);
}

function renderProjects(lang) {
  projectGrid.innerHTML = "";

  window.PORTFOLIO_PROJECTS.forEach((project) => {
    const article = document.createElement("article");
    article.className = "project-card";

    const tagList = project.tags.map((tag) => `<span>${tag}</span>`).join("");

    article.innerHTML = `
      <div class="project-card-header">
        <div>
          <p class="project-status">${project.status[lang]}</p>
          <h3>${project.title}</h3>
        </div>
        <span class="project-period">${project.period}</span>
      </div>
      <p class="project-summary">${project.summary[lang]}</p>
      <div class="project-meta">
        <strong>${translations[lang]["project.role"]}</strong>
        <p>${project.role[lang]}</p>
      </div>
      <div class="project-meta">
        <strong>${translations[lang]["project.impact"]}</strong>
        <p>${project.impact[lang]}</p>
      </div>
      <div class="tag-list">${tagList}</div>
      <a class="project-link" href="${project.url}" target="_blank" rel="noreferrer">${translations[lang]["project.open"]}</a>
    `;

    projectGrid.appendChild(article);
  });
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => translatePage(button.dataset.lang));
});

document.querySelector("#year").textContent = new Date().getFullYear();
translatePage(getInitialLanguage());
